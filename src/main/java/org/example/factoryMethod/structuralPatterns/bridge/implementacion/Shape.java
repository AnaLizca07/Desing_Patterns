/**
 * This interface represents a shape that can be drawn.
 * It provides a contract for classes that implement the drawing functionality.
 *
 * @author Ana Lizcano
 */
package org.example.factoryMethod.structuralPatterns.bridge.implementacion;

public interface Shape {
    /**
     * Draws the shape.
     * This method should be implemented by classes that implement this interface.
     */
    void draw();
}
